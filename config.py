#       NL2SQL 引擎通过向量相似度匹配，找到与用户问题最相关的表/字段说明
# ============================================================
CHROMA_CONFIG = {
    "persist_dir": "./chroma_db",           # 向量数据持久化目录
                                            # 运行 rag_init.py 后会在此目录生成向量索引文件
                                            # 数据持久化后无需每次重新构建知识库
    "collection_name": "power_schema",      # 向量集合名称
                                            # ChromaDB 中的 collection 相当于关系数据库中的"表"
                                            # 存储了所有 Schema 文档的嵌入向量及其原始文本
}

